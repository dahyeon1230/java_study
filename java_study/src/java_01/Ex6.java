package java_01;

public class Ex6 {

	public static void main(String[] args) {
		// 컴퓨터에서 자료 표현하기
		int num=10;
		int bnum=0B1010;//2진수
		int onum=012;//8진수
		int xnum=0xA;//16진수
		
		System.out.println(num);
		System.out.println(bnum);//10
		System.out.println(onum);//10
		System.out.println(xnum);//10
	}

}
