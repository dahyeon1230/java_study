package java_01;

public class Ex3 {

	public static void main(String[] args) {
		// 화면에 글자 출력하기
		
		System.out.println("====EX1====");
		
		System.out.println("Hello world"); //Hello world 출력하고 줄바꿈 한다.
		System.out.print("Hello"); //Hello 출력하고 줄바꿈 안함
		System.out.println("world");//world 출력하고 줄바꿈 함
		
		System.out.println("====EX2====");

		System.out.println("Hello world");//Hello world가 출력된다.
		System.out.print("3+5=");//3+5출력되고 줄바꿈 하지 않음
		System.out.println(3+5);//8이 출력된다.
		
		//()괄호 안에 숫자를 넣으면 계산된 결과가 출력되지만 "" 내용은 글자로 간주되어 계산되지 않고 그대로 출력된다.
		
		
	}

}
