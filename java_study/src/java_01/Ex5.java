package java_01;

public class Ex5 {

	public static void main(String[] args) {
		// 변수
		int level; //정수형 변수 level를 선언
		level=10; //level에 10을 대입 
		System.out.println(level); //level값 출력
		
		System.out.println("--------------------"); //10
		
		int num1;//변수의 선언
		num1=3;//변수에 값 대입(초기화)
		num1=5;//변수에 값 수정
		
		int num2=3;//변수의 선언과 동시에 초기화
		
		System.out.println(num1+num2); //8
		System.out.println(num1-num2); //2
		System.out.println(num1*num2); //15
		System.out.println(num1/num2); //1

	}

}
